function t = initTubules()
    global gMTProperties
    
    for i=1:length(gMTProperties)
        tubuleparams.(gMTProperties{i}) = 0;
    end
    
    emptytrack = newemptystreamline();
    
    t = struct( ...
            'tracks', emptystructarray( emptytrack ), ...
            'maxid', 0, ...
            'defaulttrack', emptytrack, ...
            'tubuleparams', tubuleparams, ...
            'statistics', struct( ...
                'created', 0, ...
                'deleted', 0, ...
                'died', 0, ...
                'spontaneouscatastrophe', 0, ...
                'spontaneousstop', 0, ...
                'zipperings', 0, ...
                'branchings', 0, ...
                'crossovers', 0, ...
                'collidecatastrophe', 0, ...
                'collidercut', 0, ...
                'collidedcut', 0, ...
                'collidestop', 0, ...
                'rescue', 0, ...
                'severings', 0, ...
                'lifetimes', zeros(0,2) ) ...
            );
end
