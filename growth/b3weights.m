function s = b3weights( bc, t )
    t = t*4;
    s12 = [ 0 0 t; 0 -t t; 0 0 -t; 0 0 0 ];
    xs12 = [ t 0 0; 0 0 0; 0 0 0; 0 -t 0 ];
    s23 = s12(:,[3 1 2]);
    s31 = s12(:,[2 3 1]);
    s13 = s12( [1 2 4 3], [1 3 2] );
    s21 = s13(:,[3 1 2]);
    s32 = s13(:,[2 3 1]);
    
    s = [ bc; zeros(3,3) ] + ...
          bc(1)*bc(2)*(s12 + s21) + ...
          bc(2)*bc(3)*(s23 + s32) + ...
          bc(3)*bc(1)*(s31 + s13);
end
